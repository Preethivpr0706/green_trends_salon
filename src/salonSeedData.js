function slugify(value) {
  return String(value || "")
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, "_")
    .replace(/^_+|_+$/g, "")
    .slice(0, 48);
}

function inferArea(addressLine1) {
  const rejectTokens = /^(near|nr|opp|opposite|above|beside|next to|no\.?|plot|hig|old|first floor|ground floor|1st floor|2nd floor|3rd floor)\b/i;
  const parts = String(addressLine1 || "")
    .split(",")
    .map((v) => v.trim())
    .filter(Boolean);
  const candidates = parts
    .filter((part) => !rejectTokens.test(part))
    .filter((part) => !/^\d+/.test(part))
    .filter((part) => part.length <= 40);
  if (candidates.length) {
    return candidates[candidates.length - 1];
  }
  return parts.length ? parts[Math.max(0, parts.length - 2)] : "";
}

export function getSeedSalons() {
  const lines = RAW_SALON_TEXT.split("\n")
    .map((v) => v.trim())
    .filter(Boolean)
    .filter((v) => v.toLowerCase() !== "green trends branches in chennai");

  const list = [];
  const usedIds = new Set();

  for (let i = 0; i + 4 < lines.length; i += 5) {
    const name = lines[i];
    const addressLine1 = lines[i + 1];
    const cityPincode = lines[i + 2];
    const openHours = lines[i + 3];
    const phone = lines[i + 4];

    const cityMatch = cityPincode.match(/^(.+?)\s*-\s*(\d{6})$/);
    if (!cityMatch) {
      continue;
    }

    const city = cityMatch[1].trim();
    const pincode = cityMatch[2].trim();
    const baseId = `salon_${slugify(`${name}_${pincode}`) || `seed_${list.length + 1}`}`;
    let id = baseId;
    let suffix = 1;
    while (usedIds.has(id)) {
      suffix += 1;
      id = `${baseId}_${suffix}`;
    }
    usedIds.add(id);

    const addressParts = [addressLine1, city, pincode].filter(Boolean).join(", ");
    list.push({
      id,
      name,
      addressLine1,
      area: inferArea(addressLine1),
      city,
      state: "Tamil Nadu",
      pincode,
      phone,
      openHours,
      lat: null,
      lng: null,
      mapsUrl: `https://maps.google.com/?q=${encodeURIComponent(addressParts)}`
    });
  }

  return list;
}
